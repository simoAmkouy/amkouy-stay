/** The dictionary's *shape* with every leaf widened to `string` — used to type `ar.ts` (which
 * must have the same keys but obviously different values) without TypeScript trying to force it
 * to contain these exact French literal strings. `typeof fr` (with its `as const`) is used
 * separately in `i18n/index.ts` to derive the literal dotted-path key union. */
type Widen<T> = { [K in keyof T]: T[K] extends string ? string : Widen<T[K]> };
export type TranslationDict = Widen<typeof fr>;
